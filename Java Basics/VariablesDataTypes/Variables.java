package VariablesDataTypes;


public class Variables {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        char n = 'a';
        String name = "Turwash";
        
        System.out.println(a);
        System.out.println(b);
        System.out.println(n);
        System.out.println(name);

        a = 30 ; // Updating the value of a 
        System.out.println(a);
    }
    
}


