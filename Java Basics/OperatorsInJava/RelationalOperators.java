package OperatorsInJava;
// Relational Operators
public class RelationalOperators {
    public static void main(String[] args) {
        
        int a = 10;
        int b = 10;
        System.out.println( a == b );  // == Relational Operator
        System.out.println( a!=b); // != Relational Operator 
        System.out.println(a>b); //Greater than Operator
        System.out.println(a<b); //Less than Operator
        System.out.println(a>=b); //Greater than equal to Operator
        System.out.println(a<=b); //Less than equal to Operator
    }
}
