//Output in java
package VariablesDataTypes;


public class Output {
    public static void main(String[] args) {
        System.out.print("Hello World"); 
        System.out.println("Hello world");
        System.out.print("Hello World\n");

        Pattern();
        
    }
    static void Pattern () {
        System.out.println("*****");
        System.out.println("****");
        System.out.println("***");
        System.out.println("**");
        System.out.println("*");
        
    }
}



//Notes:

// ; is called statement terminator


/*  Types of Printing Statements 
 * System.out.println - Creates New Line after printing
 * System.out.print - Only Prints and does not creates new line
 * "\n" - For creating new line
*/