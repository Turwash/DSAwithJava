package OperatorsInJava;



// Assignment Operatos 
public class AssignmentOperators {
    public static void main(String[] args) {
    // += Assignment Operator Used
    int a = 10;
    a += 10;
    System.out.println(a);
    // -= Assignment Operator Used
    int b = 5;
    b -= 5;
    System.out.println(b);
    // *= Assignment Operator Used
    int c = 10;
    c *= 5;
    System.out.println(c);
    // /= Assignment Operator Used
    int d = 10;
    d /= 5;
    System.out.println(d);

    }
    

}
